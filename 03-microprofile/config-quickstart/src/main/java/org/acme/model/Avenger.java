package org.acme.model;

public class Avenger {

    public String name;
    public String civilName;
    public boolean snapped;

    public Avenger() {
    }

    public Avenger(String name, String civilName, boolean snapped) {
        this.name = name;
        this.civilName = civilName;
        this.snapped = snapped;
    }
}
